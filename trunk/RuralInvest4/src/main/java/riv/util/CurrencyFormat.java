package riv.util;

/* 
 * ALL = Groups thousands and shows decimal places
 * NOTHOUSANDS = Doesn't groups thousands, shows decimal places
 * NODECIMALS = Groups thousands, doesn't show decimal places
 * INTEGER = Doesn't group thousands, doesn't show decimal places
 * 
 */
public enum CurrencyFormat { ALL, NOTHOUSANDS, NODECIMALS, INTEGER }